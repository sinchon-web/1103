package operators;

public class Relation {
	
	public static void main(String[] args) {
		int a=10;
		int b=20;
		//참이면 true, 거짓이면 false
		System.out.println( a==b );//false
		System.out.println( a!=b );//true
		//System.out.println( a=!b );//순서 !=
		System.out.println( a>b );//false
		System.out.println( a<b );//true
		System.out.println( a>=b );//false
		System.out.println( a<=b );//true
	}
}
